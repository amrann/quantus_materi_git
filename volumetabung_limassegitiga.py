class VolumeTabung():
    def hitung(self,jarijari:float, tinggi:float):
        return 3.14*(jarijari**2)*tinggi 

class VolumeLimasSegitiga():
     def hitung(self, alassegitiga:float, tinggisegitiga:float, tinggilimas:float):
        return 0.33 * 0.5 * alassegitiga * tinggisegitiga * tinggilimas
