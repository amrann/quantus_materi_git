class LuasTabung():
    def hitung(self,jarijari:float, tinggi:float):
        return (2*3.14*(jarijari**2)) + (2*3.14*jarijari*tinggi)

class  LuasLimasSegitiga():
     def hitung(self, alassegitiga:float, tinggisegitiga:float, alasbidang1:float, 
                tinggibidang1:float, alasbidang2:float, tinggibidang2:float, alasbidang3:float, tinggibidang3:float):
        return (0.5*alassegitiga*tinggisegitiga) + (0.5*alasbidang1*tinggibidang1) + (0.5*alasbidang2*tinggibidang2) + (0.5*alasbidang3*tinggibidang3)


